package protocoltest

// PixelPusherDiscoveryPacket builds a data packet containing a discovered
// PixelPusher device.
func PixelPusherDiscoveryPacket() []byte {
	return []byte{
		0xFA, 0xCE, 0xFE, 0xED, 0x70, 0xAD, // MAC
		0x0A, 0x00, 0x00, 0x01, // IP4
		0x02,       // PixelPusherDeviceType
		0x01,       // Protocol Version
		0x37, 0x13, // VendorID
		0xB5, 0xDA, // ProductID
		0xDD, 0xCC, // Hardware Revision
		0x82, 0x00, // Software Revision (130)
		0x78, 0x56, 0x34, 0x12, // Link Speed
		0x06,       // 6 Strips Attached
		0x02,       // 2 Strips per Packet
		0x80, 0x00, // 128 pixels per strip
		0x13, 0x12, 0x11, 0x10, // Update Period
		0x23, 0x22, 0x21, 0x20, // Power Total
		0x33, 0x32, 0x31, 0x30, // Delta Sequence
		0x43, 0x42, 0x41, 0x40, // Controller Ordinal
		0x53, 0x52, 0x51, 0x50, // Group Ordinal
		0x61, 0x60, // ArtNet Universe
		0x71, 0x70, // ArtNet Channel
		0xCE, 0xFA, // Port
		0x00, 0x00, // (Padding)
		0x70, 0x71, 0x72, 0x73, 0x74, 0x75, 0x00, 0x00, // Strip Flags
		0x00, 0x00, // (Padding)
		0x88, 0x77, 0x66, 0x55, // Pusher Flags
		0x44, 0x33, 0x22, 0x11, // Segments
		0xDD, 0xCC, 0xBB, 0xAA, // Power Domain
	}
}
